print("=== โปรแกรมคำนวณพื้นที่และเส้นรอบรูปสี่เหลี่ยม ===")

กว้าง = float(input("กรอกความกว้าง: "))
ยาว = float(input("กรอกความยาว: "))

พื้นที่ = กว้าง * ยาว
เส้นรอบรูป = 2 * (กว้าง + ยาว)

print("\nผลลัพธ์")
print("พื้นที่ =", พื้นที่, "ตารางหน่วย")
print("เส้นรอบรูป =", เส้นรอบรูป, "หน่วย")

if กว้าง == ยาว:
    print("รูปนี้เป็นสี่เหลี่ยมจัตุรัส")
else:
    print("รูปนี้เป็นสี่เหลี่ยมผืนผ้า")